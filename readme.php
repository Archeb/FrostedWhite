<h3>一些说明</h3>

<p>自定义预览图使用办法：文章编辑页面下方展开【自定义自字段】，在【字段名称】中填入"preview”，在【字段值】中填入图片地址。</p>

<p>关于顶栏：在管理-独立页面中添加新页即为添加列表</p>

<p>自定义顶栏列表图标：独立页面编辑下方展开【自定义自字段】，在【字段名称】中填入"icon”，在【字段值】中填入图标名（如fa-star）。</p>

<p>主题使用的是FontAwesome，图标列表可在 <a href="http://fontawesome.io/">http://fontawesome.io/</a> 找到</p>

<p>关于友情链接：打开模板目录下sidebar.php依照格式编辑即可。推荐成对添加，最后下面两个要加bottom。</p>
